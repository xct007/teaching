"""
Contoh variabel dan variable di Python.

Contoh 1: Variabel
Contoh 2: Tipe data boolean
Contoh 3: Tipe data integer
Contoh 4: Tipe data float
Contoh 5: Tipe data string
Contoh 6: Tipe data list
Contoh 7: Tipe data tuple
Contoh 8: Tipe data dictionary
"""

""" Contoh 1: Variabel
Variabel adalah tempat penyimpanan data yang bisa berisi berbagai tipe data
"""
nama = "John"  # Variabel `nama` berisi tipe data string
umur = 30      # Variabel `umur` berisi tipe data integer

""" Contoh 2: Tipe data boolean
Tipe data boolean hanya bisa berisi True atau False
"""
isActive = True  # Variabel `isActive` berisi tipe data boolean

""" Contoh 3: Tipe data integer
Tipe data integer berisi angka bulat
"""
angka = 10  # Variabel `angka` berisi tipe data integer

""" Contoh 4: Tipe data float
Tipe data float berisi angka desimal
"""
pi = 3.14  # Variabel `pi` berisi tipe data float

""" Contoh 5: Tipe data string
Tipe data string berisi teks/karakter
"""
kata = "Halo"  # Variabel `kata` berisi tipe data string

""" Contoh 6: Tipe data list
Tipe data list berisi kumpulan data yang bisa berubah-ubah
"""
warna = ["merah", "hijau", "biru"]  # Variabel `warna` berisi tipe data list

""" Contoh 7: Tipe data tuple
Tipe data tuple berisi kumpulan data yang tidak bisa berubah-ubah
"""
koordinat = (3, 4)  # Variabel `koordinat` berisi tipe data tuple

""" Contoh 8: Tipe data dictionary
Tipe data dictionary berisi pasangan kunci-nilai
"""
orang = {
    "nama": "John",
    "umur": 30,
    "isActive": True
}  # Variabel `orang` berisi tipe data dictionary

# Menampilkan semua variabel untuk verifikasi
print("Contoh 1: Variabel")
print("Nama:", nama)
print("Umur:", umur)

print("Contoh 2: Tipe data boolean")
print("isActive:", isActive)

print("Contoh 3: Tipe data integer")
print("Angka:", angka)

print("Contoh 4: Tipe data float")
print("Pi:", pi)

print("Contoh 5: Tipe data string")
print("Kata:", kata)

print("Contoh 6: Tipe data list")
print("Warna:", warna)

print("Contoh 7: Tipe data tuple")
print("Koordinat:", koordinat)

print("Contoh 8: Tipe data dictionary")
print("Orang:", orang)
